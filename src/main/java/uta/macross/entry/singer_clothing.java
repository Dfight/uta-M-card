package uta.macross.entry;

public class singer_clothing {
    private int Singer_Clothing_ID;
    private int Singer_Clothing_SID;
    private int Singer_Clothing_CID;

    public singer_clothing() {
    }

    public singer_clothing(int singer_Clothing_ID, int singer_Clothing_SID, int singer_Clothing_CID) {
        Singer_Clothing_ID = singer_Clothing_ID;
        Singer_Clothing_SID = singer_Clothing_SID;
        Singer_Clothing_CID = singer_Clothing_CID;
    }

    public int getSinger_Clothing_ID() {
        return Singer_Clothing_ID;
    }

    public void setSinger_Clothing_ID(int singer_Clothing_ID) {
        Singer_Clothing_ID = singer_Clothing_ID;
    }

    public int getSinger_Clothing_SID() {
        return Singer_Clothing_SID;
    }

    public void setSinger_Clothing_SID(int singer_Clothing_SID) {
        Singer_Clothing_SID = singer_Clothing_SID;
    }

    public int getSinger_Clothing_CID() {
        return Singer_Clothing_CID;
    }

    public void setSinger_Clothing_CID(int singer_Clothing_CID) {
        Singer_Clothing_CID = singer_Clothing_CID;
    }
}
